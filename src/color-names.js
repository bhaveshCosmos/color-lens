/**
 * Find the closest color name for a given hex code
 * Uses Euclidean distance in RGB space
 */
function getColorName(hex) {
  hex = hex.replace('#', '').toUpperCase();
  
  // Exact match
  if (COLOR_NAMES[hex]) {
    return { name: COLOR_NAMES[hex], exact: true };
  }
  
  // Find closest match
  const r1 = parseInt(hex.substring(0, 2), 16);
  const g1 = parseInt(hex.substring(2, 4), 16);
  const b1 = parseInt(hex.substring(4, 6), 16);
  
  let minDistance = Infinity;
  let closestName = "Unknown";
  
  for (const [namedHex, name] of Object.entries(COLOR_NAMES)) {
    const r2 = parseInt(namedHex.substring(0, 2), 16);
    const g2 = parseInt(namedHex.substring(2, 4), 16);
    const b2 = parseInt(namedHex.substring(4, 6), 16);
    
    // Weighted Euclidean distance (human perception weighting)
    const distance = Math.sqrt(
      2 * Math.pow(r1 - r2, 2) +
      4 * Math.pow(g1 - g2, 2) +
      3 * Math.pow(b1 - b2, 2)
    );
    
    if (distance < minDistance) {
      minDistance = distance;
      closestName = name;
    }
  }
  
  return { name: closestName, exact: false };
}
